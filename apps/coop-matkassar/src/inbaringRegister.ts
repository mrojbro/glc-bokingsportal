/** Visible form for display/export (trim, collapse spaces). */
export function normalizeMottAdress(adress: string): string {
  return cleanAdressText(adress)
}

/** Match key: NFKC, strip invisible chars, fold accents (vägen = vagen). */
export function matchKeyForAdress(adress: string): string {
  const cleaned = cleanAdressText(adress).toLocaleLowerCase('sv')
  if (!cleaned) return ''
  return cleaned
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
}

function cleanAdressText(adress: string): string {
  return adress
    .normalize('NFKC')
    .replace(/^\uFEFF/, '')
    .replace(/\uFEFF/g, '')
    .replace(/[\u200B-\u200D\u2060]/g, '')
    .replace(/\u00A0/g, ' ')
    .trim()
    .replace(/\s+/g, ' ')
}

const INBARING_ADDRESS_KEYS = new Set(
  INBARING_REGISTER.map((entry) => matchKeyForAdress(entry.adress)),
)

/** True when Mott. Adress matches the built-in inbärningsregister. */
export function isInbaringAddress(mottAdress: string): boolean {
  const key = matchKeyForAdress(mottAdress)
  if (!key) return false
  return INBARING_ADDRESS_KEYS.has(key)
}
